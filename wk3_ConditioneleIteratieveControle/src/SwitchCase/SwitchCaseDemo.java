package SwitchCase;

public class SwitchCaseDemo {

    public static void main(String[] args) {
        int i = 7;
        switch (i) {
            case 1:
                System.out.println("een");
                break;
            case 2:
                System.out.println("twee");
                break;
            case 3:
                System.out.println("drie");
                break;
            default:
                System.out.println("onbekend");
        }

    }

}
